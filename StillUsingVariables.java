public class StillUsingVariables
{
	public static void main( String[] args )
	{
		String myName;
		int gradYear;
		
		myName = "Jessica Oberst";
		gradYear = 2010;
		
		System.out.println( "My name is " + myName + " and I graduated high school in "
			+ gradYear + "." );
	}
}

